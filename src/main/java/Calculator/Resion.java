package Calculator;

public class Resion {
	private long up;
	private long down;
	private long none;
	
	public long getUp() {
		return up;
	}
	public void setUp(long up) {
		this.up = up;
	}
	public long getDown() {
		return down;
	}
	public void setDown(long down) {
		this.down = down;
	}
	public long getNone() {
		return none;
	}
	public void setNone(long none) {
		this.none = none;
	}
}
